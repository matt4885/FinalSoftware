    private void buttonCaluculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCaluculateActionPerformed
        boolean isStatusLabelSet = false;
        //setConfirmEnabled(true);
        refreshStatusLabel();
        if (radioButtonDisperseFunds.isSelected()) {
            labelStatus.setText("New Cash Balance will be: " + (Double.parseDouble(textCash.getText()) - Double.parseDouble(textCashDispersal.getText())));
            //setConfirmEnabled(true);
            System.out.println("test ");
        }
        if (radioButtonAcceptDeposit.isSelected()) {
            labelStatus.setText("New Cash Balance will be: " + (Double.parseDouble(textCash.getText()) + Double.parseDouble(textCashDispersal.getText())));
            //setConfirmEnabled(true);
        }
        if (radioButtonSell.isSelected()) {
            if (Double.parseDouble(textQuantity.getText()) > Double.parseDouble(textStockHeld.getText())) {
                labelStatus.setText("Not Enough Stock on Hand to Sell");
                textValue.setText("");
                textTotalValue.setText("");
                isStatusLabelSet = true;
            } else {
                textValue.setText(moneyFormat.format(calculateValue()));
                textTotalValue.setText(moneyFormat.format(calculateTotalSellValue()));
                labelStatus.setText("New Cash Balance will be: " + (Double.parseDouble(textCash.getText()) - calculateTotalSellValue()));
                //setConfirmEnabled(true);
            }
        }
        if (radioButtonBuy.isSelected()) {
            textValue.setText(moneyFormat.format(calculateValue()));
            textTotalValue.setText(moneyFormat.format(calculateTotalBuyValue()));
            labelStatus.setText("New Cash Balance will be: " + (Double.parseDouble(textCash.getText()) + calculateTotalBuyValue()));
            // setConfirmEnabled(true);
        } else {
            if (!isStatusLabelSet) {
                labelStatus.setText("Please Select a Transaction Button");
            }
        }

    }//GEN-LAST:event_buttonCaluculateActionPerformed

