    public double calculateValue() {
        return (Double.parseDouble(textQuantity.getText()) * Double.parseDouble(textMarketPrice.getText()));
    }

    public double calculateTotalBuyValue() {
        return ((calculateValue() * Double.parseDouble(textCommissionRate.getText()) + calculateValue()));
    }

    public double calculateTotalSellValue() {
        return calculateValue() - (calculateValue() * Double.parseDouble(textCommissionRate.getText()));
    }

